public class MethodDemo {
    public static void main(String[] args) {
        printMultiplicationTable2();
        printMultiplicationTable2();
        printMultiplicationTable2();
    }

    public static void printMultiplicationTable2() {
        for (int i = 1; i <= 10; i++)
            System.out.println(2 * i);
    }

}
